package helloworld;

class Relational {
	public static void main(String[] args) {
		int a = 25;
		int b = 43;
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a != b);
		System.out.println(a == b);
	}
}
